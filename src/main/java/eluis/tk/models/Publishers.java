package eluis.tk.models;

public class Publishers extends EntityBase {
    public String Name;
    public String URL;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getURL() {
        return URL;
    }

    public void setURL(String URL) {
        this.URL = URL;
    }
}
